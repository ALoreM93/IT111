public class MyVariable1 {

    public static void main(String[] args){
        String name = "Lore"; // after a statement, don't forget the semi colon!
        int hours = 40;  // we are assigning 40 to the hours variable
        // Please note, when we have a string value, it is nested in ""
        // when we are referring to numbers/integers, no quotes!!!
        System.out.println(name);
        System.out.println(hours);

        String building, tree, lake, ocean;
        tree = " Redwood";
        building = " Miami Beach";
        lake = " Lake Washington";
        ocean = " Pacific Ocean";

        int weeks, decades, months, years;
        weeks = 4;
        decades = 10;
        months = 3;
        years =5;

        System.out.println(tree);
        System.out.print(building);
        System.out.print(lake);
        System.out.print(ocean);
        System.out.print(weeks);
        System.out.print(decades);
        System.out.print(months);
        System.out.print(years);

        System.out.println();

    }
}
